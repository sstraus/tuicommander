import { createTabManager, type BaseTab } from "./tabManager";
import { currentBranchKey, repositoriesStore } from "./repositories";

// Zoom bounds mirror the terminal zoom (useTerminalLifecycle) for consistency.
const MD_MIN_FONT_SIZE = 8;
const MD_MAX_FONT_SIZE = 32;
const MD_FONT_STEP = 2;
/** Default matches #markdown-content font-size: var(--font-lg) = 15px in styles.css */
export const MD_DEFAULT_FONT_SIZE = 15;

// ---------------------------------------------------------------------------
// Types
// ---------------------------------------------------------------------------

/** File-based markdown tab (reads content from the filesystem via Tauri) */
export interface FileTab extends BaseTab {
  type: "file";
  repoPath: string;
  filePath: string;
  /** Display name (basename of filePath) */
  fileName: string;
  /** Effective filesystem root for I/O (worktree path when on a linked worktree, otherwise same as repoPath) */
  fsRoot?: string;
}

/** Virtual markdown tab (content generated by a plugin via markdownProviderRegistry) */
export interface VirtualTab extends BaseTab {
  type: "virtual";
  title: string;
  /** URI routed through markdownProviderRegistry, e.g. "plan:file?path=/foo.md" */
  contentUri: string;
}

/** Plugin panel tab (renders HTML in a sandboxed iframe via PluginPanel component) */
export interface PluginPanelTab extends BaseTab {
  type: "plugin-panel";
  title: string;
  /** The plugin that owns this panel */
  pluginId: string;
  /** HTML content rendered inside the sandboxed iframe */
  html: string;
  /** Optional URL to load instead of inline HTML (mutually exclusive with html) */
  url?: string;
}

/** Native Claude Usage Dashboard tab */
export interface ClaudeUsageTab extends BaseTab {
  type: "claude-usage";
  title: string;
}

/** PR diff tab (renders a full PR diff in the main panel) */
export interface PrDiffTab extends BaseTab {
  type: "pr-diff";
  title: string;
  repoPath: string;
  prNumber: number;
  prTitle: string;
  /** Raw unified diff content */
  diff: string;
}

/** HTML preview tab (renders HTML in a sandboxed iframe) */
export interface HtmlPreviewTab extends BaseTab {
  type: "html-preview";
  title: string;
  repoPath: string;
  filePath: string;
  fileName: string;
  fsRoot?: string;
}

/** Native Command Overview panel tab */
export interface CommandOverviewTab extends BaseTab {
  type: "command-overview";
  title: string;
}

/** Discriminated union of all markdown tab types */
export type MdTabData = FileTab | VirtualTab | PluginPanelTab | ClaudeUsageTab | PrDiffTab | HtmlPreviewTab | CommandOverviewTab;

/**
 * Resolve a caller-supplied path (repo root or PTY cwd) to a registered repo
 * path. Exact match first, then longest-prefix match so that nested cwds
 * (e.g. `/repo/src/foo`) still map back to the repo root (`/repo`).
 * Returns null when no registered repo owns the path.
 */
export function resolveRepoForCwd(cwd: string | null | undefined): string | null {
  if (!cwd) return null;
  const repos = Object.keys(repositoriesStore.state.repositories);
  if (repos.includes(cwd)) return cwd;
  let best: string | null = null;
  for (const repo of repos) {
    const prefix = repo.endsWith("/") ? repo : `${repo}/`;
    if (cwd.startsWith(prefix) && (best === null || repo.length > best.length)) {
      best = repo;
    }
  }
  return best;
}

